package com.bridgelabz;


public class Vowel_or_Consonant {
    public static void main(String[] args) {
        char a= 'i';

        if(a == 'a' || a == 'e' || a == 'i' || a == 'o' || a == 'u' )

            System.out.println(a + " is vowel");
        else{
            System.out.println(a + " is consonant");
        }
    }
}
